    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jTextArea1.getText().isEmpty()|| jTextField1.getText().isEmpty())
        JOptionPane.showMessageDialog(this, "Please Enter a Message First! and a Number Password");
        else{
            String message = jTextArea1.getText();
            int pass = Integer.parseInt(jTextField1.getText());
            char[] mess = message.toCharArray();

            StringBuilder sb = new StringBuilder();
            for(char c : mess){
                sb.append(c = (char) (c - pass));
            }
            jTextArea2.setText(sb.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

